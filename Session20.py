"""
    Built In Python Modules
"""

import math
# import math as m # alias name for the usage

print("PI is:", math.pi)
print("e is:", math.e)

print("log 1000 is:", math.log10(1000))
print("sqrt of 16 is:", math.sqrt(16))
print("factorial of 7 is:", math.factorial(7))
print("2 power 5 is:", math.pow(2, 5))

print("12.2 floor value is:", math.floor(12.2))
print("12.2 ceil value is:", math.ceil(12.2))

